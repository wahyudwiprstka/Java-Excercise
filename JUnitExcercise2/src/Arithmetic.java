public class Arithmetic {
    public int addition(int a, int b){
        return a+b;
    }

    public int substraction(int a, int b){
        return a-b;
    }

    public int multiplication(int a, int b){
        return a*b;
    }

    public float division(float a, float b){
        return a/b;
    }

    public int modulus(int a, int b){
        return a%b;
    }
}
